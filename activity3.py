# Sample string
my_string = "  Hello, Python World!  "

# 1. Convert to uppercase
upper_case_string = my_string.upper()
print("Uppercase:", upper_case_string)

# 2. Convert to lowercase
lower_case_string = my_string.lower()
print("Lowercase:", lower_case_string)

# 3. Replace part of the string
replaced_string = my_string.replace("Python", "Java")
print("Replaced string:", replaced_string)

# 4. Split the string into words
split_string = my_string.split()
print("Split string:", split_string)

# 5. Remove leading and trailing whitespace
stripped_string = my_string.strip()
print("Stripped string:", stripped_string)
